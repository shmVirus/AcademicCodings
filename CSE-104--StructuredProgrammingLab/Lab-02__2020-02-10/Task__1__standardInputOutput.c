/*Program for scanning and printing.*/
#include<stdio.h>
int main(void)
{
    int a;              // "int" is the "keyword" and "a" is the "identifier" for integer data type.
    scanf("%d", &a);    // "scanf" is the function for scanning and "%d" is the "specifier" for integer data type.
    printf("%i\n",a);   // "printf" is the function for printing. Integer has one more "specifier" that is "%i".
    return 0;
}
